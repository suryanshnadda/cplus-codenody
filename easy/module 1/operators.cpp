#include<iostream>
using namespace std;
int main()
{
	int a = 10; //constant
	int b = 5;  //constant
	
	cout<<"a = "<<a<<endl;
	cout<<"b = "<<b<<"\n--------"<<endl;
	cout<<"a-b = "<<a-b<<endl;
	cout<<"a+b = "<<a+b<<endl;
	cout<<"a/b = "<<a/b<<endl;
	cout<<"a%b = "<<a%b<<endl;
	cout<<"a*b = "<<a*b<<endl;
	
}

