#include <iostream>
using namespace std;
 
int main () {
   // number definition:
   short  sh;
   double dou;
   int    in;
   long   lo;
   float  fl;
   
   
   // number assignments;
   sh = 3;      
   in = 1000;    
   lo = 1000000; 
   fl = 223.47;  
   dou = 12949.374;
   
   // number printing;
   cout << "short  s :" << sh << endl;
   cout << "int    i :" << in << endl;
   cout << "long   l :" << lo << endl;
   cout << "float  f :" << fl << endl;
   cout << "double d :" << dou << endl;
 
   return 0;
}

